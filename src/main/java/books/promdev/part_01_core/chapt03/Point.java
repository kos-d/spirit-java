package books.promdev.part_01_core.chapt03;

public class Point {
    private final double x;
    private final double y;
    
    public Point(double xx, double yy) {
        super();
        x = xx;
        y = yy;
    }
    
    public double getX() {
        return x;
    }
     public double getY() {
         return y;
     }
}