package goit.module02_primitive.conspectus.video.lesson03_scanner;

public class PrintlnMethodExample {
    public static void main(String[] args) {
        
        int x = 10;
        int y = 20;
        int z = x + y;
        System.out.println("Sum of x and y is " + z);
        System.out.printf("Sum of x and y is %d \n", z);
        
        System.out.println("%d %f %x %e %c %s");
        
        System.out.println("Hello World");
        System.out.print("Hello World \n"); // windows delimiter \r
        System.out.printf("Hello World");
    }
}