package goit.module03.conspectus.materials.theme06;

public class WhileApp {
    public static void main(String[] args) {
        
        int number = 1;
        while(number < 10) {
            System.out.println(number);
            number++;
        }
        
        System.out.println("------------");
        
        int num = 1;
        do {
            System.out.println(num);
            num++;
        } while(num < 10);
        
        
        
    }
}