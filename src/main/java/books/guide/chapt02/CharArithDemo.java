package books.guide.chapt02;

public class CharArithDemo {
    public static void main(String[] args) {
        char ch;
        
        ch = 'X';
        System.out.println("ch contains " + ch);
        
        ch++;
        System.out.println("now ch contains " + ch);
        
        ch = 90;
        System.out.println("now ch contains " + ch);
    }
}