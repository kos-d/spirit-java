package goit.module06.conspectus.materials.theme01;

public class Point {
    private final int x;
    private final int y;
    
    public int getX() {
        return x;
    }
    
    public int getY() {
        return y;
    }
    
    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }
}