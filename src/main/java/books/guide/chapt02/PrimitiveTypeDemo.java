package books.guide.chapt02;

public class PrimitiveTypeDemo {

    public static void main(String[] args) {
        String[] primitiveType
                = {"boolean",
                    "byte",
                    "char",
                    "double",
                    "float",
                    "int",
                    "long",
                    "short"};

        for (String s : primitiveType) {
            System.out.println(s);
        }
    }
}
