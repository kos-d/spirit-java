package module02.conspectus.materials.theme05;

public class LogicalAnd {
    public static void main(String[] args) {
        // Записывается символами &&. Результат будет true, если ОБА операнда - true
        
        boolean x = true;
        boolean y = false;
        
        boolean result = x && y;
        
        System.out.println(result);
    }
}