package goit.module02_primitive.conspectus.materials.theme03;

public class AssignmentOperatorExample {
    public static void main(String[] args) {
        // Оператор присвоения
        // assignment operator
        int appleCount = 3;                             // 3
        int bananaCount = 5 + 6;                        // 11
        int totalFruitCount = appleCount + bananaCount; // 14
        
        System.out.println(totalFruitCount);
    }
}